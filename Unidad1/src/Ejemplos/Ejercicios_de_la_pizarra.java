package Ejemplos;

public class Ejercicios_de_la_pizarra {

	public static void main(String[] args) {
		double x, x1, x2, x3;
		
		x = (Math.pow(5, 2)+ 10) * 3;
		System.out.println("((5^2)+10)*3 = "+ x);
		
		x1 = 35.3 - 53 / 10;
		System.out.println("35.3 - 53 / 10 = "+ x1);
		
		x2 = 1 + 5%1.5 * 8 / 2;
		System.out.println("1 + 5%1.5 * 8 / 2 = "+ x2);
		
		x3 = -(-Math.pow(2, 5)+18 / 5);
		System.out.println("-(-Math.pow(2, 5)+18 / 5) = "+ x3);
		
		
	}

}
