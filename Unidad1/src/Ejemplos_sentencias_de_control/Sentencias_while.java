package Ejemplos_sentencias_de_control;

public class Sentencias_while {

	public static void main(String[] args) {
		int cont = 0;
		
		while(cont < 5){
			System.out.println(cont);
			cont++;
		}
		
		int cont1 = 0;
		while(cont1 < 5){
			System.out.println(cont1);
			cont1++;
		}
		
		int cont2 = 0;
		while(cont2 < 5){
			System.out.println(cont2);
			cont2++;
		}
	}
}


